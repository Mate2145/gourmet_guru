import 'dart:ui';

import 'package:flutter/material.dart';
import 'package:flutter/services.dart';
import 'package:gourmet_guru/constants/colors.dart';

class RecipeDetailAppBar extends StatelessWidget {
  final String? foodPicture;

  RecipeDetailAppBar({Key? key, required this.foodPicture}) : super(key: key);

  @override
  Widget build(BuildContext context) {
    return SliverAppBar(
        systemOverlayStyle:
            const SystemUiOverlayStyle(statusBarBrightness: Brightness.dark),
        expandedHeight: 275.0,
        backgroundColor: Colors.white,
        elevation: 0.0,
        pinned: true,
        stretch: true,
        flexibleSpace: this.foodPicture != null
            ? FlexibleSpaceBar(
                background: Image.network(
                  this.foodPicture!,
                  fit: BoxFit.cover,
                ),
                stretchModes: const [
                  StretchMode.blurBackground,
                  StretchMode.zoomBackground,
                ],
              )
            : FlexibleSpaceBar(
                background: Container(
                  color: ggPrimaryColor,
                ),
                stretchModes: const [
                  StretchMode.blurBackground,
                  StretchMode.zoomBackground,
                ],
              ),
        bottom: PreferredSize(
          preferredSize: const Size.fromHeight(0.0),
          child: Container(
            height: 32.0,
            alignment: Alignment.center,
            decoration: const BoxDecoration(
              color: Colors.white,
              borderRadius: BorderRadius.only(
                topLeft: Radius.circular(32.0),
                topRight: Radius.circular(32.0),
              ),
            ),
            child: Container(
              width: 40.0,
              height: 5.0,
              decoration: BoxDecoration(
                color: Colors.black,
                borderRadius: BorderRadius.circular(100.0),
              ),
            ),
          ),
        ),
        leadingWidth: 80.0);
  }
}
